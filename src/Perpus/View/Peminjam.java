    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        String tgl;

        tgl = "2015" + "/" + (jComboBox2.getSelectedIndex()+1)
        + "/" + jComboBox3.getSelectedItem();
        EnPinjam enp = new EnPinjam();
        enp.setIdpinjam(Integer.parseInt(txtIdpinjam.getText()));
        enp.setIdbuku(Integer.parseInt(txtIdbuku.getText()));
        enp.setNmpinjam(txtnama.getText());
        enp.setTgl_pinjam(tgl);
        enp.setNotelp(txtNotelp.getText());
        enp.setAlamat(txtalamat.getText());
 

        
        if (Proses.equalsIgnoreCase("INSERT")){
            Status = daoPinjam.Inpinjam(enp);
            Pesan = "penyimpanan data sukses ";
        }
        else {
            Status = daoPinjam.Uppinjam(enp);
            Pesan = "penyimpanan data sukses ";
        }

        if (Status == false){
            Pesan = "penyimpanan data gagal ";
        }
        JOptionPane.showMessageDialog(null, Pesan ,
            "Informasi",JOptionPane.INFORMATION_MESSAGE);
        refresh();
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
         // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(null,"Apakah anda yakin menghapus data ini ?","Komfimasi",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION){
            Status = daoPinjam.Delpinjam(txtIdpinjam.getText());
            if(Status == false){
                Pesan = "Hapus data gagal";
            }
            JOptionPane.showMessageDialog(null, Pesan, "Informasi",
                JOptionPane.INFORMATION_MESSAGE);
        }
        refresh();
    }//GEN-LAST:event_bhapusActionPerformed

