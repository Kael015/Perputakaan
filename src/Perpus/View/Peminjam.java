  private void AturTeks(boolean b){
      txtIdpinjam.setEnabled(b);
      txtIdbuku.setVisible(b);
      txtnama.setVisible(b);
      txtNotelp.setVisible(b);
      txtalamat.setVisible(b);
      jComboBox2.setVisible(b);
      jComboBox3.setVisible(b);
  }
  public void refresh(){
      ListPinjam = daoPinjam.selPinjam();
      dtm = (DefaultTableModel) tabelpinjam.getModel();
      dtm.setRowCount(0);
      for (EnPinjam data : ListPinjam){
          dtm.addRow(new Object[]{data.getIdpinjam(),data.getIdbuku(),data.getNmpinjam(),data.getTgl_pinjam()
          ,data.getNotelp(),data.getAlamat()});
      }
      if(tabelpinjam.getRowCount()>0){
          baris = tabelpinjam.getRowCount()-1;
          tabelpinjam.setRowSelectionInterval(baris, baris);
          AturTeks(true);
          Proses="";
      }
  }
